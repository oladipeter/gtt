Az önálló fejlesztő, gyártó, tevékenység eredményeként saját termékeink köre évről évre bővül. A meglevő termékek hardverét folyamatosan fejlesztjük, a programok szolgáltatásait bővítjük. Cégünk által nyújtott szolgáltatásokat egyre több megrendelő veszi igénybe.


Minőségi eredmények:


A Globomax Kft. megalakulása óta folyamatosan arra törekszik, hogy termékeinek és szolgáltatásainak minőségét javítsa. 2000-ben bevezetésre került az ISO 9001 minőségbiztosítási rendszer, amelyet évente külső független céggel auditáltattunk. 2003-ban ez átdolgozásra került, és azóta az új ISO-9001/2000 minőségirányítási rendszer alapján dolgozik a cég.


