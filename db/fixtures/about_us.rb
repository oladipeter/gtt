Küldetésünk:


A Globomax termékei és szolgáltatásai járuljanak hozzá ahhoz, hogy a hazai demokrácia és önkormányzatiság kiváló lehessen a döntéshozatali folyamatok terén. Ehhez Magyar Termék Nagydíjas hardvereket és szoftvereket fejlesztünk és bocsátunk az önkormányzatok rendelkezésére. Szakembereink több évtizedes tapasztalata kapcsán sokszor jobban tudjuk, mire van szüksége az ügyfélnek, mint saját maga az ügyfél. Hiszünk abban, hogy ügyfeleink céljainak elérésén keresztül a magunk módján hozzájárulhatunk az önkormányzati munka transzparenciájához. Hisszük, hogy munkánkkal csökkenthetjük a lakosság, a választópolgárok, és politika távolságát.



Előzmények:


A Globomax 1995-ben alakult.

2000 áprilisa óta a Globomax Kft. 100%-os magántulajdonban van. Tulajdonosai kivétel nélkül a dolgozók, ill. a céggel közvetlen munkakapcsolatban álló magánszemélyek.
2002-ben saját tulajdonú, budapesti telephelyet vásárolt a cég, amelyet folyamatosan fejleszt, bővít a tevékenység és a szervezet igényei szerint.



