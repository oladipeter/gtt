Víziónk, azon jövőképünk teljesülése, hogy nincs olyan tízezer lakosnál nagyobb hazai település, amelynek önkormányzata ne ismerné a Globomax-ot. Ezen túlmenően ne tudná saját tapasztalatai, vagy ismerős önkormányzati vezetők beszámolói alapján teljes bizonyossággal, hogy a Globomax név garancia arra, hogy kiváló eszközöket és szolgáltatásokat nyújt az önkormányzati döntéshozatali folyamatok támogatására.

